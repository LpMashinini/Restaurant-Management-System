    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Low stock button
        jButton1.addActionListener(e -> {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            DefaultTableModel filtered = new DefaultTableModel(
                    new Object[][]{},
                    new String[]{"Item ID", "Item Name", "Quantity", "Date"}
            );

            for (int i = 0; i < model.getRowCount(); i++) {
                int qty = Integer.parseInt(model.getValueAt(i, 2).toString());
                if (qty <= 5) {
                    filtered.addRow(new Object[]{
                        model.getValueAt(i, 0),
                        model.getValueAt(i, 1),
                        qty,
                        model.getValueAt(i, 3)
                    });
                }
            }

            jTable1.setModel(filtered);
            jTable1.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
                @Override
                public Component getTableCellRendererComponent(JTable table, Object value,
                        boolean isSelected, boolean hasFocus, int row, int column) {

                    Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

                    int quantityColumn = 2; // Assuming column 2 is Quantity

                    Object qtyValue = table.getValueAt(row, quantityColumn);

                    try {
                        int quantity = Integer.parseInt(qtyValue.toString());

                        if (quantity < 5) {
                            c.setBackground(new java.awt.Color(255, 102, 102)); // light red
                        } else {
                            c.setBackground(isSelected ? table.getSelectionBackground() : java.awt.Color.WHITE);
                        }

                    } catch (Exception e) {
                        c.setBackground(java.awt.Color.WHITE); // fallback
                    }

                    return c;
                }
            });

        });

    }//GEN-LAST:event_jButton1ActionPerformed

