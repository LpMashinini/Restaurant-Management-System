    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        String itemName = jTextField1.getText();
        String itemId = jTextField2.getText();
        int itemQuantity = (Integer) jSpinner1.getValue();

        if (itemId.isEmpty() || itemName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;
        }

        try {
            // 1️⃣ Add to table view
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(new Object[]{itemId, itemName, itemQuantity});

            // 2️⃣ Save to MySQL database
            Connection conn = DBConnection.connect();
            if (conn != null) {
                String sql = "INSERT INTO inventory (item_id, item_name, quantity) VALUES (?, ?, ?)";
                PreparedStatement stmt = conn.prepareStatement(sql);

                stmt.setString(1, itemId);
                stmt.setString(2, itemName);
                stmt.setInt(3, itemQuantity);

                int result = stmt.executeUpdate();

                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "✅ Item saved to database!");
                } else {
                    JOptionPane.showMessageDialog(this, "❌ Failed to save item to database.");
                }

                conn.close();
            } else {
                JOptionPane.showMessageDialog(this, "❌ Database connection failed.");
            }

            // 3️⃣ Feedback to user
            JOptionPane.showMessageDialog(this, "Item added:\nName: " + itemName + "\nID: " + itemId + "\nQuantity: " + itemQuantity);

            // 4️⃣ Clear input fields
            jTextField1.setText("");
            jTextField2.setText("");
            jSpinner1.setValue(0);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "❌ Quantity must be a valid number.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "❌ SQL Error: " + e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "❌ Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton12ActionPerformed

