    public void loadInventoryData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear previous table data

        try {
            Connection conn = DBConnection.connect();
            String sql = "SELECT item_id, item_name, quantity, date_added FROM inventory";
            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                String id = rs.getString("item_id");
                String name = rs.getString("item_name");
                int quantity = rs.getInt("quantity");
                String date = rs.getDate("date_added").toString();

                model.addRow(new Object[]{id, name, quantity, date});
            }

            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "‚ùå Error loading stock: " + e.getMessage());
        }
    }

    public void highlightLowStock() {
        jTable1.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable table, Object value,
                    boolean isSelected, boolean hasFocus, int row, int column) {

                Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);

                try {
                    int quantity = Integer.parseInt(table.getValueAt(row, 2).toString()); // column 2 = quantity

                    if (quantity < 5) {
                        c.setBackground(new java.awt.Color(255, 102, 102)); // light red
                    } else {
                        c.setBackground(isSelected ? table.getSelectionBackground() : java.awt.Color.WHITE);
                    }
                } catch (Exception e) {
                    c.setBackground(java.awt.Color.WHITE);
                }

                return c;
            }
        });
    }

